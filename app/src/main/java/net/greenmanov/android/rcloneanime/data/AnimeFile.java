package net.greenmanov.android.rcloneanime.data;

public class AnimeFile {
    private String name;
    private Anime anime;

    public AnimeFile() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Anime getAnime() {
        return anime;
    }

    public void setAnime(Anime anime) {
        this.anime = anime;
    }
}
